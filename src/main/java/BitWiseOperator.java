public class BitWiseOperator {
    public static void main(String[] args){
        // Initial values
        int a = 5;
        int b = 7;

        // Bitwise AND
        // 0101 & 0111=0101
        System.out.println("a & b = " + (a & b));
        // Bitwise OR
        // 0101 | 0111=0111
        System.out.println("a | b = " + (a | b));
    }
}
